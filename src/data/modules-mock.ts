export function getModuleRecords(moduleId: ModuleId) {
  return MODULE_RECORDS.filter((r) => r.moduleId === moduleId);
}

export function getModuleRecord(id: string) {
  return MODULE_RECORDS.find((r) => r.id === id);
}

export function getMyModuleRecords(userId: string) {
  return MODULE_RECORDS.filter((r) => r.requester.id === userId);
}

export function getModuleDetailPath(record: ModuleRecord) {
  return `${MODULE_CONFIGS[record.moduleId].basePath}/${record.id}`;
}
